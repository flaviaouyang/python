courses = ["history", "philosophy", "geography", "calculus"]

for index, course in enumerate(courses, start=1):
  print("Course number: ", index)
  print("Course: ", course)
  
# output:
# Course number:  1
# Course:  history
# Course number:  2
# Course:  philosophy
# Course number:  3
# Course:  geography
# Course number:  4
# Course:  calculus